    private void buttonBoHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBoHangActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableGioHang.getModel();
        int i = tableGioHang.getSelectedRow();
            if (i >= 0) {
            iOS spIOS = new iOS();
            Android spAndroid = new Android();
            iOS ios = BLLsp.BLLtimIOS(model.getValueAt(i, 0).toString());
            if(ios != null){
                spIOS.setMaSanPham(model.getValueAt(i, 0).toString());
                int sl = ios.getSoLuong();
                int slGH = Integer.valueOf(model.getValueAt(i, 2).toString());
                int slupdate = sl + slGH;
                BLLsp.BLLsuaSLIOS(spIOS, slupdate);
                loadIOSList();
                list.remove(spIOS);
            }
            Android and = BLLsp.BLLtimANDROID(model.getValueAt(i, 0).toString());
            if(and != null){
                spAndroid.setMaSanPham(model.getValueAt(i, 0).toString());
                int sl = and.getSoLuong();
                int slGH = Integer.valueOf(model.getValueAt(i, 2).toString());
                int slupdate = sl + slGH;
                BLLsp.BLLsuaSLAND(spAndroid, slupdate);
                loadANDROIDList();
                list.remove(spAndroid);
            }
            model.removeRow(i);
        }
        
    }//GEN-LAST:event_buttonBoHangActionPerformed

