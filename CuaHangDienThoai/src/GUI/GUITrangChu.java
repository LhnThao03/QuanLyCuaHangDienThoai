    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        // TODO add your handling code here:
        modelIOS = (DefaultTableModel) tableIOS.getModel();
        modelADNROID = (DefaultTableModel) tableANDROID.getModel();
        int i=tableIOS.getSelectedRow();
        int j=tableANDROID.getSelectedRow();
        if (i>=0){
            iOS sp = new iOS();
            sp.setMaSanPham( modelIOS.getValueAt(i, 0).toString());
            sp.setTenSanPham(modelIOS.getValueAt(i, 1).toString());
            sp.setDonGia(Float.valueOf(modelIOS.getValueAt(i, 3).toString()));
            sp.setNamSX(Integer.valueOf(modelIOS.getValueAt(i, 4).toString()));
            sp.setPhienbanios(Float.valueOf(modelIOS.getValueAt(i, 5).toString()));
            String sl = JOptionPane.showInputDialog("Nhập số lượng");
            String tonkho = modelIOS.getValueAt(i, 2).toString();
            sp.setSoLuong(Integer.valueOf(sl));
            int newSL = Integer.valueOf(tonkho) - Integer.valueOf(sl);
            if(newSL < 0){
                JOptionPane.showMessageDialog(null, "Quá số lượng tồn kho! Mời bạn nhập lại số lượng");
            }else{
            modelGioHang = (DefaultTableModel) tableGioHang.getModel();
            modelGioHang.addRow(new Object[]{sp.getMaSanPham(), sp.getTenSanPham(), sl, sp.getDonGia(), sp.getNamSX(),sp.getPhienbanios()});
            list.add(sp);
            BLLsp.BLLsuaSLIOS(sp,newSL);
            loadIOSList();
            }
        }else if (j>=0){
            Android sp = new Android();
            sp.setMaSanPham( modelADNROID.getValueAt(j, 0).toString());
            sp.setTenSanPham(modelADNROID.getValueAt(j, 1).toString());
            sp.setDonGia(Float.valueOf(modelADNROID.getValueAt(j, 3).toString()));
            sp.setNamSX(Integer.valueOf(modelADNROID.getValueAt(j, 4).toString()));
            sp.setHangSanXuat(modelADNROID.getValueAt(j, 5).toString());
            String sl = JOptionPane.showInputDialog("Nhập số lượng");
            String tonkho = modelADNROID.getValueAt(j, 2).toString();
            sp.setSoLuong(Integer.valueOf(sl));
            int newSL = Integer.valueOf(tonkho) - Integer.valueOf(sl);
            if(newSL < 0){
                JOptionPane.showMessageDialog(null, "Quá số lượng tồn kho! Mời bạn nhập lại số lượng");
            }else{
            modelGioHang = (DefaultTableModel) tableGioHang.getModel();
            modelGioHang.addRow(new Object[]{sp.getMaSanPham(), sp.getTenSanPham(), sl, sp.getDonGia(), sp.getNamSX(),sp.getHangSanXuat()});
            list.add(sp);
            BLLsp.BLLsuaSLAND(sp,newSL);
            loadANDROIDList();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
        }
    }//GEN-LAST:event_buttonAddActionPerformed

    public void loadIOSList(){
        modelIOS = new DefaultTableModel();
        modelIOS.addColumn("Mã SP");
        modelIOS.addColumn("Tên sản phẩm");
        modelIOS.addColumn("Số lượng");
        modelIOS.addColumn("Đơn giá");
        modelIOS.addColumn("Năm SX");
        modelIOS.addColumn("Phiên bản");
        modelIOS.addColumn("Img");
        tableIOS.setModel(modelIOS);
        column1 = tableIOS.getColumnModel().getColumn(1);
        column1.setPreferredWidth(150);
        ArrayList<DTOSanPham> arr = new ArrayList<DTOSanPham>();
        arr = BLLsp.BLLgetIOS();
        for(int i = 0; i < arr.size(); i++){
            DTOSanPham sp = arr.get(i);
            if(sp instanceof iOS){
                iOS ios = (iOS) sp;
                String masp = ios.getMaSanPham();
                String tensp = ios.getTenSanPham();
                Integer sl = ios.getSoLuong();
                Float dongia = ios.getDonGia();
                Integer nsx = ios.getNamSX();
                Float pb = ios.getPhienbanios();
                String img = ios.getImg();
                Object[] row = {masp,tensp,sl,dongia,nsx,pb,img};
                modelIOS.addRow(row);
            }
        }
    }
    
